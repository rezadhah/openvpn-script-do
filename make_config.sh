#!/bin/bash

# First argument: Client identifier
cwd=$PWD
KEY_DIR=$PWD/openvpn-ca/keys
OUTPUT_DIR=$PWD/client-configs-${1}/files
BASE_CONFIG=$PWD/client-configs-${1}/base.conf

cat ${BASE_CONFIG} \
    <(echo -e '<ca>') \
    ${KEY_DIR}/ca.crt \
    <(echo -e '</ca>\n<cert>') \
    ${KEY_DIR}/${1}.crt \
    <(echo -e '</cert>\n<key>') \
    ${KEY_DIR}/${1}.key \
    <(echo -e '</key>\n<tls-auth>') \
    ${KEY_DIR}/ta.key \
    <(echo -e '</tls-auth>') \
    > ${OUTPUT_DIR}/${1}.ovpn
